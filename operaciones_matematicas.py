#Operadores numéricos
numero_uno = 10
numero_dos = 3

#Suma
print('Suma: ', numero_uno + numero_dos)

#Resta
print('Resta: ', numero_uno - numero_dos)

#Multiplicación
print('Multiplicación: ', numero_uno * numero_dos)

#Potenciación
print('Potenciación: ', numero_uno ** numero_dos)

#Divicion
print('Divicion: ', numero_uno / numero_dos)


#Módulo 
print('Módulo: ', numero_uno % numero_dos)


#Parte entera de la division 
print('Parte entera de la division: ', numero_uno // numero_dos)


#Atajos
numero_uno = numero_uno + 2
print('Atajos: ', numero_uno)


#Reglas PEMDAS

#P parentheses
#E Exponents
#M multiply
#D Divide
#A Add
#S subtract

operation_one = 2 + 3 * 4 
operation_two = 2 + (3 * 4) 

print('Operaticíon 1: ', numero_uno)
print('Operaticíon 2: ', operation_two)


#Booleanos
print('Booleanos: ', numero_uno > numero_dos)
print('Booleanos: ', numero_uno < numero_dos)
print('Booleanos: ', numero_uno >= numero_dos)
print('Booleanos: ', numero_uno <= numero_dos)
print('Booleanos: ', numero_uno == numero_dos)
print('Booleanos: ', numero_uno != numero_dos)
